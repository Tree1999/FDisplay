void EEPROM_Data_Init(void)
{
    UINT8   i;
    UINT8   read_buf[10]={0};
    
    EEPROM_I2C_Init();

    /* the last postion of read_buf is keep to 0 */
    EEPROM_I2C_Master_Read(0x00, read_buf, 4);

    for(i=0; i<0xFF; i++);
    
    read_buf[0]=0x11;
    read_buf[1]=0x22;
    EEPROM_I2C_Master_Write(0x00, read_buf, 2);

    read_buf[0]=0x00;
    read_buf[1]=0x00;
    for(i=0; i<0xFF; i++);
    
    EEPROM_I2C_Master_Read(0x00, read_buf, 2);

}
