/***********************************************************************************************
*
* @brief    deserialize_if_init() - Program entry function
* @param    none
* @return   none
*
************************************************************************************************/
void CTP_Data_Write(void)
{
    UINT8 write_buf[242] ;

    memset(write_buf, 0x00, 0xF2);
    write_buf[0] = 0xD0;    /* write address, LSB */
    write_buf[1] = 0x00;    /* write address, MSB */
    write_buf[2] = 0x00;    /* write data, data[0] */
    write_buf[3] = 0x00;    /* write data, data[1] */
    write_buf[4] = 0x00;    /* write data, data[2] */

    CTP_I2C_Master_Send(&write_buf[0],0xF2);
    
}

/***********************************************************************************************
*
* @brief    deserialize_if_init() - Program entry function
* @param    none
* @return   none
*
************************************************************************************************/
void CTP_Data_Read(void)
{
    UINT8 write_buf[2] = {0};
    UINT8 send_buf[2] = {0};

    write_buf[0] = 0xD0;
    send_buf[1] = 0x00;
    
    CTP_I2C_Master_Send(write_buf,0x02);
    CTP_I2C_Master_Read(send_buf,0x02);
}
